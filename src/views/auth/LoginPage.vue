<script setup lang="ts">
import { ref } from 'vue'
import { useRoute, useRouter } from 'vue-router'
import Layout from '@/components/layout/Layout.vue'
import DanjiInput from '@/components/common/form/DanjiInput.vue'
import { Lock, User, ChevronRight } from 'lucide-vue-next'
import { login } from '@/api/auth'
import type { LoginRequest, LoginResponse } from '@/types/auth'
import axios from 'axios'
import { queryClient } from '@/api/queryClient'
import { instance } from '@/api/api'

const route = useRoute()
const router = useRouter()

const username = ref('')
const password = ref('')
const isLoading = ref(false)
const errorMessage = ref('')

async function onLogin() {
  if (isLoading.value) return
  isLoading.value = true
  errorMessage.value = ''

  try {
    const payload: LoginRequest = { username: username.value, password: password.value }
    const res: LoginResponse = await login(payload)
    const { accessToken } = res

    localStorage.setItem('accessToken', accessToken)
    instance.defaults.headers.common.Authorization = `Bearer ${accessToken}`

    const redirect = (route.query.redirect as string) || '/home'
    await router.replace(redirect)

    window.location.assign(redirect)

    void queryClient.invalidateQueries({ queryKey: ['member'], exact: true }).catch(() => {})
  } catch (err: unknown) {
    console.error('로그인 오류:', err)
    if (axios.isAxiosError(err)) {
      errorMessage.value = err.response?.data?.message || '로그인에 실패했습니다.'
    } else if (err instanceof Error) {
      errorMessage.value = err.message
    } else {
      errorMessage.value = '알 수 없는 오류가 발생했습니다.'
    }
  } finally {
    isLoading.value = false
  }
}
</script>

<template>
  <Layout header-type="basic" :is-bottom-nav="false" :show-left-icon="false">
    <template #content>
      <div class="flex flex-col items-center bg-white box-border p-[24px]">
        <div class="text-center mt-[24px] mb-[32px]">
          <img
            src="@/assets/images/danji-logo-main.png"
            alt="단지 로고"
            class="h-[60px] mb-[12px] ml-[50px]"
          />
          <p class="text-[14px] leading-[1.6] text-[#666666] mt-[30px]">
            단 하나의 지갑이면 충분해요<br />
            모든 지갑을 하나로, 단지에서 시작하세요 😊
          </p>
        </div>

        <form
          class="w-[365px] bg-white rounded-[12px] p-[24px] flex flex-col gap-[3px] mt-[25px]"
          @submit.prevent="onLogin"
        >
          <div class="h-[64px] flex items-center relative">
            <danji-input
              v-model="username"
              placeholder="아이디를 입력해주세요."
              aria-label="아이디"
              autocomplete="username"
            >
              <template #icon>
                <User
                  class="absolute left-[25px] top-1/2 -translate-y-1/2 w-[20px] h-[20px] pointer-events-none text-[#d9d9d9]"
                />
              </template>
            </danji-input>
          </div>

          <div class="h-[64px] flex items-center relative">
            <danji-input
              v-model="password"
              type="password"
              placeholder="비밀번호를 입력해주세요."
              aria-label="비밀번호"
              autocomplete="current-password"
            >
              <template #icon>
                <Lock
                  class="absolute left-[25px] top-1/2 -translate-y-1/2 w-[20px] h-[20px] pointer-events-none text-[#d9d9d9]"
                />
              </template>
            </danji-input>
          </div>

          <router-link
            to="/signup"
            class="flex items-center justify-end text-[1.09rem] text-[#c7c7c7] mt-[1rem] mb-[-1.95rem] mr-[0.625rem]"
          >
            단지의 첫 지갑을 만들어볼까요?
            <span class="mx-[4px]"></span>
            <strong class="font-normal">회원가입</strong>
            <ChevronRight class="w-[14px] h-[14px] stroke-[2.5] text-[#c7c7c7]" />
          </router-link>

          <button
            type="submit"
            class="w-full max-w-[360px] mt-[20px] py-[16px] text-[16px] font-medium text-white bg-[#60584c] rounded-[12px] cursor-pointer disabled:bg-[#b9b3ab] disabled:cursor-not-allowed"
            :disabled="isLoading"
          >
            로그인
          </button>

          <p v-if="errorMessage" class="mt-3 text-[13px] text-red-500 text-center">
            {{ errorMessage }}
          </p>
        </form>
      </div>
    </template>
  </Layout>
</template>

